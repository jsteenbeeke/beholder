package com.jeroensteenbeeke.topiroll.beholder.web.pages.dungeonmaster;

import org.apache.wicket.markup.html.link.Link;

public class OverviewPage extends AuthenticatedPage {

	private static final long serialVersionUID = 1L;


	public OverviewPage() {
		super("Overview");

		add(new Link<Void>("prepare") {
			private static final long serialVersionUID = 8114233117435162078L;

			@Override
			public void onClick() {
				setResponsePage(new PrepareSessionPage());
			}
		});

		add(new Link<Void>("run") {
			private static final long serialVersionUID = -2497972640867242254L;

			@Override
			public void onClick() {
				setResponsePage(new RunSessionPage());
			}
		});


	}



}
